package com.lethalcoders.movieapi.domain.movie.models;

import com.lethalcoders.movieapi.domain.actor.models.Actor;
import com.lethalcoders.movieapi.domain.core.models.BaseClass;

import javax.persistence.Entity;
import java.util.Date;
import java.util.List;

@Entity
public class Movie extends BaseClass {

    private String title;
    private List<Actor> actors;
    private String description;
    private Integer durationInMins;
    private String language;
    private Date releaseDate;
    private String country;
    private String genre;

    public Movie(){

    }

    public Movie(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public List<Actor> getActors() {
        return actors;
    }

    public void setActors(List<Actor> actors) {
        this.actors = actors;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Integer getDurationInMins() {
        return durationInMins;
    }

    public void setDurationInMins(Integer durationInMins) {
        this.durationInMins = durationInMins;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public Date getReleaseDate() {
        return releaseDate;
    }

    public void setReleaseDate(Date releaseDate) {
        this.releaseDate = releaseDate;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }
}